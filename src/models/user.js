import mongoose from 'mongoose';
const { Schema } = mongoose;

// create the user schema
const userSchema = new Schema(
  {
    firstName: {
      type: String,
      required: true,
      trim: true,
    },
    lastName: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    timestamps: true,
  }
);

// create the user model
const User = mongoose.model('User', userSchema);

// export the user model
export { User };
